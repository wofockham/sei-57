<template>
    <form action="#" @submit.prevent="onSubmit">
        <p v-if="errorsPresent" class="error">Please fill out both fields</p>
        <div class="labeled input fluid">
            <div class="ui label">
                <i class="germany flag" /> German
            </div>
            <input placeholder="Enter German word" v-model.trim="word.german" />
        </div>
        <div class="labeled input fluid">
            <div class="ui label">
                <i class="united kingdom flag" /> English
            </div>
            <input placeholder="Enter English word" v-model.trim="word.english" />
        </div>

        <button class="positive ui button">Submit</button>
    </form>
</template>

<script>
export default {
    name: 'word-form',
    props: {
        word: {
            type: Object,
            required: false,
            default() {
                return {
                    english: '',
                    german: ''
                };
            }
        }
    },
    data() {
        return {
            errorsPresent: false
        }
    },
    methods: {
        onSubmit() {
            if (this.word.english === '' || this.word.german === '') {
                this.errorsPresent = true;
            } else {
                this.$emit('createOrUpdate', this.word);
            }
        }
    }
}
</script>

<style>
.error {
    color: red;
}
</style>